package q4;

public class Quadrado {
	double lado ;
	
	public double getQuadrado(){
		return lado;
	}
	
	public void quadrado(double lado){
		this.lado = lado;
	}
	
	
	public double getArea(){
		return lado;
	}
	public double area(){
		return (lado*lado);
	}
	
	
	
	public double getPerimetro(){
		return lado;
	}
	public double perimetro(){
		return (4*lado);
	}
}
